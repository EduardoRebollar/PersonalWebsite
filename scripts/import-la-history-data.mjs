// One-shot importer: reads the source JSON from the gitignored Personal Data
// directory and emits typed TS modules under content/data/laHistory/.
// Run once after cloning, or whenever the source data changes.
//
//   bun run scripts/import-la-history-data.mjs

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const SRC = resolve(
  ROOT,
  'Personal Data',
  'Personal Projects',
  'LA History',
  'data',
);
const OUT = resolve(ROOT, 'content', 'data', 'laHistory');

mkdirSync(OUT, { recursive: true });

const HEADER =
  '// AUTO-GENERATED by scripts/import-la-history-data.mjs.\n' +
  '// Do not edit by hand — re-run the importer instead.\n\n';

function camel(snake) {
  return snake.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
}

function camelKeys(obj) {
  if (Array.isArray(obj)) return obj.map(camelKeys);
  if (obj && typeof obj === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(obj)) {
      if (v === null || v === undefined) continue;
      out[camel(k)] = camelKeys(v);
    }
    return out;
  }
  return obj;
}

function emitLocations() {
  const raw = JSON.parse(readFileSync(resolve(SRC, 'locations.json'), 'utf8'));
  const locations = raw.map((loc) => {
    const c = camelKeys(loc);
    // Original points to /static/img/...; our public assets live under
    // /la-history/img/... once copied. Rewrite the URL prefix so the data
    // module references stable, portable paths.
    if (c.imageUrl && c.imageUrl.startsWith('/static/img/')) {
      c.imageUrl = c.imageUrl.replace('/static/img/', '/la-history/img/');
    }
    return c;
  });
  const body =
    HEADER +
    `import type { Location } from '@/types/laHistory';\n\n` +
    `export const locations = ${JSON.stringify(locations, null, 2)} as const satisfies readonly Location[];\n\n` +
    `export const locationsBySlug = new Map(locations.map((l) => [l.slug, l]));\n` +
    `export const locationsById = new Map(locations.map((l) => [l.id, l]));\n`;
  writeFileSync(resolve(OUT, 'locations.ts'), body);
  console.log(`✓ wrote ${locations.length} locations`);
}

function emitQuizzes() {
  const raw = JSON.parse(readFileSync(resolve(SRC, 'quizzes.json'), 'utf8'));
  const quizzes = raw.map(camelKeys);
  const body =
    HEADER +
    `import type { Quiz } from '@/types/laHistory';\n\n` +
    `export const quizzes = ${JSON.stringify(quizzes, null, 2)} as const satisfies readonly Quiz[];\n\n` +
    `export const quizzesByLocationSlug = new Map(quizzes.map((q) => [q.locationSlug, q]));\n`;
  writeFileSync(resolve(OUT, 'quizzes.ts'), body);
  console.log(`✓ wrote ${quizzes.length} quizzes`);
}

